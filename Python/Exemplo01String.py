#Exemplo de mudança de letras e junção de palavras

#Nome estilo título
nome = "ada lovelace"
print(nome.title())


nome = "Ada Lovelace"
#Nome maíusculo 
print(nome.upper())
#Nome minusculo
print(nome.lower())


#Junção de palavras
primeiro_nome = "ada"
sobrenome = "lovelace"
nome_completo = primeiro_nome + " " + sobrenome
print(nome_completo)